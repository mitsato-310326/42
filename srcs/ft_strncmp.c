#include <stddef.h>
#include <stdlib.h>

int ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t i = 0;
	if (n == 0)
		return(0);
	while(*s1 == *s2 && ++i < n)
	{
		++s1;
		++s2;
	}
	return(*s1 - *s2);
}
